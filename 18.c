#include<stdio.h>
int main()
{
    int day_no;
    scanf("%d",&day_no);
    if(day_no==1)
    printf("monday");
    else if(day_no==2)
    printf("tuesday");
    else if(day_no==3)
    printf("wednesday");
    else if(day_no==4)
    printf("thursday");
    else if(day_no==5)
    printf("friday");
    else if(day_no==6)
    printf("saturday");
    else
    printf("sunday");
    
}
